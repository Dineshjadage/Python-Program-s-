"""program to demonstrate functions"""

def sum(a,b):
    res=(a+b)
    print("sum is",res)

def diff(a,b):
    res=(a-b)
    print("diff is",res)
    
def mul(a,b):
    res=(a*b)
    print("mul is",res)
    
def div(a,b):
    res=(a/b)
    print("div is",res)

sum(5,3)
diff(5,3)
mul(5,3)
div(5,3)
    
"""output:
sum is 8
diff is 2
mul is 15
div is 1.6666666666666667"""
